package dto;

public class MangerHistory {
	private String mangerId;
	private String team;
	private String teamId;
	private String appoint;
	private String constract;
	private String position;
	private String matches;
	private String pointPerMatch;
	
	
	public String getMangerId() {
		return mangerId;
	}
	public void setMangerId(String mangerId) {
		this.mangerId = mangerId;
	}
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getTeamId() {
		return teamId;
	}
	public void setTeamId(String teamId) {
		this.teamId = teamId;
	}
	public String getAppoint() {
		return appoint;
	}
	public void setAppoint(String appoint) {
		this.appoint = appoint;
	}
	public String getConstract() {
		return constract;
	}
	public void setConstract(String constract) {
		this.constract = constract;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getMatches() {
		return matches;
	}
	public void setMatches(String matches) {
		this.matches = matches;
	}
	public String getPointPerMatch() {
		return pointPerMatch;
	}
	public void setPointPerMatch(String pointPerMatch) {
		this.pointPerMatch = pointPerMatch;
	}


}
